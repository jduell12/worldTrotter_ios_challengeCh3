import UIKit

extension UIColor {
    struct Custom {
        struct Blue {
            static let Teal = UIColor(rgb: 0x29B2E1)
            static let Aqua = UIColor(rgb: 0x1A93BD)
        }
        
        struct Green {
            static let LimeGreen = UIColor(rgb: 0x3AD42B)
        }
        
        struct Red {
            static let Pink = UIColor(rgb: 0xC52BD4)
        }
        
        struct Grey {
            static let GreyBrown = UIColor(rgb: 0xF5F4F1)
            static let DarkGrey = UIColor(rgb: 0xF1F2F5)
            static let Grey = UIColor(rgb: 0x6D7892)
        }
    }
}
